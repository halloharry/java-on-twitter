package twitter.curriculumbasic.basicjava.variable;

public class ContohVariable7 {
    public static void main(String[] args) {
        // Menggunakan operator + untuk penjumlahan
        int hasilPenjumlahan = 5 + 3;

        // Menggunakan operator - untuk pengurangan
        int hasilPengurangan = 10 - 2;

        // Menggunakan operator * untuk perkalian
        int hasilPerkalian = 4 * 6;

        // Menggunakan operator / untuk pembagian
        double hasilPembagian = 20.0 / 4;

        // Menggunakan operator % untuk modulus
        int hasilModulus = 17 % 5;

        // Menampilkan hasil dari operasi-operasi di atas
        System.out.println();
        System.out.println("Hasil penjumlahan: " + hasilPenjumlahan);
        System.out.println("Hasil pengurangan: " + hasilPengurangan);
        System.out.println("Hasil perkalian: " + hasilPerkalian);
        System.out.println("Hasil pembagian: " + hasilPembagian);
        System.out.println("Hasil modulus: " + hasilModulus);
    }
}
