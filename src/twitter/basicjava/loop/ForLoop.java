package twitter.basicjava.loop;

public class ForLoop {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("Iterasi ke-" + i);
        }
        // kita jabarin ya bre..
        /**
         * int i = 0; adalah inisialisasi variabel i.
         * Ini akan dieksekusi hanya sekali pada awal perulangan.
         * i < 5; adalah kondisi yang akan dievaluasi sebelum setiap iterasi.
         * Jika kondisi ini salah, perulangan akan berhenti.
         * i++ adalah langkah inkrementasi (penambahan).
         * Ini akan dieksekusi setelah setiap iterasi.
         * nanti kita lihat hasil nya di console bre !

         */
    }

}
