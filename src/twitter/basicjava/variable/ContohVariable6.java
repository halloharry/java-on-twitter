package twitter.basicjava.variable;

public class ContohVariable6 {
    public static void main(String[] args) {
        // Mendeklarasikan variabel dan memberikan nilai awal
        int umur = 25;

        // Mengubah nilai variabel
        umur = 26;

        // Menampilkan nilai variabel yang telah diperbarui
        System.out.println("Umur sekarang: " + umur);
    }
}
